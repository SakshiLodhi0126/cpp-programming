#include <iostream>
using namespace std;

// Demonstrating basic data types
int main() {
  int a = 10;
  float b = 5.5;
  char c = 'A';
  bool d = true;

  cout << "Integer: " << a << endl;
  cout << "Float: " << b << endl;
  cout << "Char: " << c << endl;
  cout << "Boolean: " << d << endl;

  return 0;
}