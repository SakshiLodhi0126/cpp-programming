#include <iostream>
using namespace std;

int main() {
  int a = 2466;
  cout << a << endl;

  // long
  long b = 2348363863;
  cout << b << endl;

  // long long
  long long c = 346473647687324;
  cout << c << endl;

  // float
  float d = 34.566;
  cout << d << endl;

  // double
  double e = 3.576732454;
  cout << e * 34.5 << endl;

  // string
  string f = "Sakshi";
  cout << f << endl;

  // size of data types
  cout << sizeof(a) << endl;
  cout << sizeof(b) << endl;
  cout << sizeof(c) << endl;
  cout << sizeof(d) << endl;
  cout << sizeof(e) << endl;
  cout << sizeof(f) << endl;

  return 0;
}
